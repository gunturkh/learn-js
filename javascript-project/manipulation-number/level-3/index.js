var numberA = ["H","E","L","L","O"];





var result = numberA.toString(); // result: a,b,c,d
console.log(result);

var result = numberA.join("_"); // result: a_b_c_d
console.log(result);

var result = numberA.length; // result: 5
console.log(result);

var result = numberA.includes("E"); // result: true
console.log(result);

var result = numberA.push("!"); // result: 5, with alphabet being ["a", "b", "c", "d", "e"]
console.log(result);

var result = numberA.pop(); // result: e, with alphabet being ["a", "b", "c", "d"] again
console.log(result);




